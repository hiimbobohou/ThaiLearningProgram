let questionInput, questionDisplay;

// 等待 DOM 加载完成
document.addEventListener('DOMContentLoaded', function () {
    questionInput = document.getElementById('questionInput');
    questionDisplay = document.getElementById('questionDisplay');

    questionInput.addEventListener('input', function () {
        if (this.value.trim() !== '') {
            questionDisplay.textContent = this.value;
        } else {
            questionDisplay.textContent = '请先输入问题';
        }
    });
});

function drawCard() {
    if (questionInput.value.trim() === '') {
        questionDisplay.textContent = '请先输入你的问题！';
        questionInput.focus();
        return;
    }

    let selectedConsonant;
    let words;
    let selectedWord = "暂无相关单词";

    do {
        const randomIndex = Math.floor(Math.random() * thaiConsonants.length);
        selectedConsonant = thaiConsonants[randomIndex];
        words = positiveConsonantWords[selectedConsonant];
    } while (!words || words.length === 0);

    if (words && words.length > 0) {
        const randomWordIndex = Math.floor(Math.random() * words.length);
        selectedWord = words[randomWordIndex];
    }

    const probability = Math.random();
    let isYes;
    if (probability < 0.7) {
        isYes = true;
    } else {
        isYes = false;
    }

    let interpretation = "";
    if (isYes) {
        interpretation = yesInterpretations[selectedConsonant] || "是的，这是一个非常积极的征象！";
    } else {
        interpretation = noInterpretations[selectedConsonant] || "不，当前情况可能需要你更加谨慎。";
    }

    const cardNameElement = document.getElementById('cardName');
    const cardMeaningElement = document.getElementById('cardMeaning');
    const cardInterpretationElement = document.getElementById('cardInterpretation');
    const cardDisplayElement = document.getElementById('cardDisplay');
    const cardBackElement = document.getElementById('cardBackPlaceholder');

    cardNameElement.textContent = selectedConsonant;
    cardMeaningElement.textContent = selectedWord;
    cardInterpretationElement.textContent = interpretation;

    cardInterpretationElement.className = '';
    cardInterpretationElement.classList.add(isYes ? 'yes' : 'no');

    cardDisplayElement.classList.add('show');
    cardBackElement.style.display = 'none';
}



